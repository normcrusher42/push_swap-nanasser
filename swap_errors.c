void	error_check(char *str)
{
	if (!((*str == '-' || *str == '+') || (*str >= '0' && *str <= '9')))
		return (1);
	if ((*str == '-' || *str == '+') && !(*str >= '0' && *str <= '9'))
		return (1);
	while (++*str)
	{
		if (!(*str >= '0' && *str <= '9'))
			return (1);
	}
	return (0);
}